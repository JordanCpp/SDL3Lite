#include <assert.h>
#include <SDL3/New.hpp>
#include <SDL3/App.hpp>
#include <SDL3/GLWin.hpp>
#include <SDL3/SoftWin.hpp>

SDL_Window* SDL_CreateWindowImplementation(Vector<SDL_Window*>& windows, OpenGLAttributes& openGLAttributes, Result& result, EventHandler& eventHandler, const char* title, int w, int h, SDL_WindowFlags flags)
{
	SDL_Window* window = NULL;

	if (flags == SDL_WINDOW_OPENGL)
	{
		window = new OpenGLWindow(openGLAttributes, result, eventHandler, Vec2i(0, 0), Vec2i(w, h), title, flags);
	}
	else
	{
		window = new SoftwareWindow(result, eventHandler, Vec2i(0, 0), Vec2i(w, h), title, flags);
	}

	if (window)
	{
		windows.push_back(window);
	}

	return window;
}

SDL_Window* SDL_CreateWindow(const char* title, int w, int h, size_t flags)
{
	return SDL_CreateWindowImplementation(
		GetApplication().GetWindows(),
		GetApplication().GetOpenGLAttributes(),
		GetApplication().GetResult(),
		GetApplication().GetEventHandler(),
		title, w, h, flags);
}

void SDL_DestroyWindow(SDL_Window* window)
{
	assert(window);

	delete window;
}

SDL_Surface* SDL_GetWindowSurface(SDL_Window* window)
{
	assert(window);

	return window->GetSurface();
}

bool SDL_UpdateWindowSurface(SDL_Window* window)
{
	assert(window);

	return window->Present();
}

const char* SDL_GetWindowTitle(SDL_Window* window)
{
	assert(window);

	return window->GetTitle().c_str();
}

bool SDL_SetWindowTitle(SDL_Window* window, const char* title)
{
	assert(window);

	window->SetTitle(title);

	return true;
}

bool SDL_GetWindowSize(SDL_Window* window, int* w, int* h)
{
	assert(window);

	Vec2i size = window->GetSize();

	*w = *&size.x;
	*h = *&size.y;

	return true;
}
